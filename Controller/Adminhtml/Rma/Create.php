namespace ViraXpress\Rma\Controller\Adminhtml\Rma;

use Magento\Backend\App\Action;
use Magento\Framework\View\Result\PageFactory;

/**
 * Adminhtml controller for creating a new RMA request page.
 *
 * Displays the form page to create a new RMA in the admin panel.
 */
class Create extends Action
{
    /**
     * @var PageFactory
     */
    protected $resultPageFactory;

    /**
     * Constructor
     *
     * @param Action\Context $context
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Action\Context $context,
        PageFactory $resultPageFactory
    ) {
        parent::__construct($context);
        $this->resultPageFactory = $resultPageFactory;
    }

    /**
     * Execute method
     *
     * Creates and returns the admin page for creating a new RMA.
     *
     * @return \Magento\Framework\View\Result\Page
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('ViraXpress_Rma::rma_create');
        $resultPage->getConfig()->getTitle()->prepend(__('Create New RMA'));
        return $resultPage;
    }
}
